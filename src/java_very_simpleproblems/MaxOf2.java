package java_very_simpleproblems;
import java.lang.*;
import java.util.*;

public class MaxOf2 {
        public static void main(String[] args) {

            Scanner puja=new Scanner(System.in);
            int A=puja.nextInt();
            int B=puja.nextInt();

            if (A>=B) {
                System.out.println(A);
            }
            else {
                System.out.println(B);
            }
        }
    }

