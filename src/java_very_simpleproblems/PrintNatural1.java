package java_very_simpleproblems;

import java.lang.*;
import java.util.*;

//print all natural numbers from 1 to N
public class PrintNatural1 {
        public static void main(String args[]) {
            Scanner sc=new Scanner(System.in);
            int N=sc.nextInt();
            for (int i=1; i<=N; i++) {
                System.out.println(i);
            }

        }
    }

