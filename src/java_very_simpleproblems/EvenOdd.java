package java_very_simpleproblems;
import java.lang.*;
import java.util.*;
public class EvenOdd {
        public static void main(String[] args) {
            Scanner puja=new Scanner(System.in);
            int N=puja.nextInt();
            if (N % 2 !=0) {
                System.out.println("1");
            }
            else {
                System.out.println("0");
            }
        }
    }

